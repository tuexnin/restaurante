    public void guardar(){
        tp=new TipoPlatoBL();
        tp.Guardar(txtTipo.getText());
        this.Listar("");
    }
    
    public void actualizar(){
        tp=new TipoPlatoBL();
        tp.Actualizar(Integer.parseInt(txtNumero.getText()), txtTipo.getText());
        this.Listar("");
    }
    
    public void Eliminar(){
        tp=new TipoPlatoBL();
        tp.Eliminar(Integer.parseInt(txtNumero.getText()));
        this.Listar("");
    }

    public void Listar(String dato){
        tp=new TipoPlatoBL();
        tp.Listar(Lista, dato);
    }

