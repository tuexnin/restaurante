    public void Listar(String dato){
        tb=new Tipo_bebidasBL();
        tb.Listar(lista, dato);
    }
    
    public void Guardar(){
        tb=new Tipo_bebidasBL();
        tb.Guardar(txtTipo.getText());
        this.Listar("");
    }
    
    public void Actualizar(){
        tb=new Tipo_bebidasBL();
        tb.Actualizar(Integer.parseInt(txtIdTipoBebida.getText()), txtTipo.getText());
        this.Listar("");
    }
    
    public void Eliminar(){
        tb=new Tipo_bebidasBL();
        tb.Eliminar(Integer.parseInt(txtIdTipoBebida.getText()));
        this.Listar("");
    }
    
    public String SacarID(String dato){
        tb=new Tipo_bebidasBL();
        return tb.SacarIDTipoBebida(dato);
    }
    
